function test(url, callback) {
  var child;
  var command = "java -jar ~/bin/alto-client.jar " + url;
  child = exec(command, function (error, stdout, stderr) {
    if (error === null) {
      callback(stdout);
    } else {
      callback(error);
    }
  });
}

exports.get_test_data = function(req, res, callback) {
  if (validateUrl(req.body.url)) {
    test(req.body.url, callback);
  } else {
    callback("Invalid Url.")
  }
}
